package com.wy.form;

public class Word {
	private Integer id=-1;
	private String word_name="";	
	private Integer word_number=-1;
	private Integer word_result=-1;
	public Integer getWord_result() {
		return word_result;
	}
	public void setWord_result(Integer word_result) {
		this.word_result = word_result;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getWord_name() {
		return word_name;
	}
	public void setWord_name(String word_name) {
		this.word_name = word_name;
	}
	public Integer getWord_number() {
		return word_number;
	}
	public void setWord_number(Integer word_number) {
		this.word_number = word_number;
	}
	

}
